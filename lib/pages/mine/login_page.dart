  void _onSignInButtonPress() {
    _pageController.animateToPage(0,
        duration: Duration(milliseconds: 500), curve: Curves.decelerate);
  }

  void _onSignUpButtonPress() {
    _pageController?.animateToPage(1,
        duration: Duration(milliseconds: 500), curve: Curves.decelerate);
  }

  void _toggleLogin() {
    setState(() {
      _obscureTextLogin = !_obscureTextLogin;
    });
  }

  void _toggleSignupConfirm() {
    setState(() {
      _obscureTextSignupConfirm = !_obscureTextSignupConfirm;
    });
  }

  void _loginSubmit() {
    String phone = loginPhoneController.text;
    String password = loginPasswordController.text;

    if (phone == null || password == null || phone.length == 0) {
      showInSnackBar("密码错误");
    } else {
      setState(() {
        showLoading = true;
      });
      _loginRequest(phone, password).then((response) {
        setState(() {
          showLoading = false;
        });
        if (response.data["code"] == 1000) {
          Navigator.of(context).pushAndRemoveUntil(
              PageRouteBuilder<Null>(
                pageBuilder: (BuildContext context, Animation<double> animation,
                    Animation<double> secondaryAnimation) {
                  return AnimatedBuilder(
                    animation: animation,
                    builder: (BuildContext context, Widget child) {
                      return Opacity(
                        opacity: animation.value,
                        child: MainPage(title: 'Dear Offer'),
                      );
                    },
                  );
                },
                transitionDuration: Duration(milliseconds: 300),
              ),
              (Route route) => route == null);
          CommonUtil.setShared("login", "logined");
          CommonUtil.setShared("token", response.data["data"]["token"]);
          CommonUtil.setShared("userName", response.data["data"]["userName"]);
        } else {
          showInSnackBar("密码错误");
        }
      });
    }
  }

  Future _loginRequest(String phone, String password) async {
    Map dataMap = Map();
    dataMap["phone"] = phone;
    dataMap["password"] = password;

    Response response = await Dio().post(Config.AUTH_LOGIN_URL, data: dataMap);

    return response;
  }

  void _getCode() {
    if (!canGetCode) {
      return;
    }
    String phone = signupPhoneController.text;
    if (phone.length != 11) {
      showInSnackBar("手机号码不正确");
    } else {
      setState(() {
        showLoading = true;
      });
      _getCodeRequest(phone).then((response) {
        setState(() {
          showLoading = false;
        });
        if (response.data["code"] == 1000) {
          showInSnackBar("已发送，请查收");
          reGetCountdown();
          setState(() {
            canGetCode = false;
          });
        } else {
          showInSnackBar(response.data["message"]);
        }
      });
    }
  }

  Future _getCodeRequest(String phone) async {
    return await Dio().get(Config.AUTH_CODE_URL + phone);
  }

  void _registerSubmit() {
    String name = signupNameController.text;
    String phone = signupPhoneController.text;
    String code = signupCodeController.text ?? "";
    String password = signupPasswordController.text;
    if (name.isEmpty || phone.isEmpty || code.isEmpty || password.isEmpty) {
      showInSnackBar("请完善信息");
    } else {
      setState(() {
        showLoading = true;
      });
      _registerRequest(name, phone, code, password).then((response) {
        setState(() {
          showLoading = false;
        });
        loginPhoneController.text = phone;
        loginPasswordController.text = password;
        _loginSubmit();
      });
    }
  }

  Future _registerRequest(
      String name, String phone, String code, String password) async {
    Map dataMap = Map();
    dataMap["userName"] = name;
    dataMap["phone"] = phone;
    dataMap["password"] = password;

    Response response =
        await Dio().post(Config.AUTH_REGISTER_URL + code, data: dataMap);
    setState(() {
      showLoading = false;
    });
    if (response.data["code"] == 1000) {
      showInSnackBar("注册成功");
    } else {
      showInSnackBar(response.data["message"]);
    }
  }
